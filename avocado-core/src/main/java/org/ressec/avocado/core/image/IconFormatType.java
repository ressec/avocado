package org.ressec.avocado.core.image;

/**
 * An enumeration to expose the available values for the icon format types.
 * @author <a href="mailto:christophe.resse@gmail.com">Christophe Resse</a>
 * @version 1.0.0
 */
public enum IconFormatType
{
    /**
     * Default icon file format.
     */
    FORMAT_DEFAULT,

    /**
     * Square icon file format.
     */
    FORMAT_SQUARE,

    /**
     * Wave icon file format.
     */
    FORMAT_WAVE,

    /**
     * Round icon file format.
     */
    FORMAT_ROUND;
}
